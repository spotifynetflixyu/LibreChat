CREATE OR REPLACE FUNCTION steel.record_price_history()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  IF OLD.unit_price IS DISTINCT FROM NEW.unit_price THEN
    INSERT INTO steel.price_history (
      price_item_id,
      old_unit_price,
      new_unit_price,
      import_log_id
    )
