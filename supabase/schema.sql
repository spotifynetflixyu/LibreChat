CREATE OR REPLACE FUNCTION steel.record_price_history()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  IF OLD.unit_price IS DISTINCT FROM NEW.unit_price THEN
    INSERT INTO steel.price_history (
      price_item_id,
      old_unit_price,
      new_unit_price,
      import_log_id
    )
    VALUES (
      NEW.id,
      OLD.unit_price,
      NEW.unit_price,
      NEW.last_import_log_id
    );
  END IF;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS record_price_history ON steel.price_items;
CREATE TRIGGER record_price_history
AFTER UPDATE OF unit_price ON steel.price_items
FOR EACH ROW
EXECUTE FUNCTION steel.record_price_history();

