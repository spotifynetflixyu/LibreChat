function matchCatalogRule(name: string, rules: readonly CatalogRule[]): string | undefined {
  return rules.find(([, pattern]) => pattern.test(name))?.[0];
}

export function inferCatalogSubcategory(
  category: string,
  productName: string | null | undefined,
): string | undefined {
  const name = productName?.normalize('NFKC').trim();
  if (!name) {
    return undefined;
  }

  if (category === '五金/配件') {
    return matchCatalogRule(name, hardwareRules) ?? '其他';
  }
  if (category === '門窗/門板') {
    return matchCatalogRule(name, doorAndWindowRules) ?? '其他';
  }
  if (category === '捲門/伸縮門') {
    return matchCatalogRule(name, rollingDoorRules) ?? '其他';
  }
  if (category === '其他') {
    return matchCatalogRule(name, miscellaneousRules) ?? '其他';
  }

  return undefined;
}
