export function inferStructuralSubcategory(
  category: string,
  productName: string,
): string | undefined {
  const normalizedName = productName.normalize('NFKC').trim();
  if (!normalizedName) {
    return undefined;
  }

  return structuralSubcategoryRules[category]?.find(([, pattern]) =>
    pattern.test(normalizedName),
  )?.[0];
}
